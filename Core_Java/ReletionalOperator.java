public class ReletionalOperator{
    public static void main(String args[]){
        int x=6;
        int y=5;
        int z=6;
        boolean result=x>y;
        System.out.println(result);

        result=x<y;
        System.out.println(result);

        result=x>z;
        System.out.println(result);

        result=x<z;
        System.out.println(result);

        result=x==z;
        System.out.println(result);

        result=x<=z;
        System.out.println(result);

        result=x>=z;
        System.out.println(result);
    }
}